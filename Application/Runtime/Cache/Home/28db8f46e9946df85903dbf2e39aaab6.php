                       <div>
                        <h4> 房屋装修注意事项</h4>

                           <p>1、关于面砖阳角部分的处理方法，归根到底是看工人的水平。如果泥水工工人水平不错，而且磨瓷砖的工具比较好的话，就应该毫不犹豫的选择磨45度角的做法。从效果上来看，只要磨的好，磨45度角的阳角做法，是最漂亮的！如果工人的水平确实不怎么样，那么你还是选择用阳角条吧，因为磨的不好的45度角做法还不如用阳角条的效果。</p>

                           <p>2、排好水管后的水管加压测试也是非常重要的。测试时，大家一定要在场，而且测试时间至少在30分钟以上，条件许可的，最好一个小时。10公斤加压，最后没有任何减少方可测试通过。</p>

                           <p>3、鞋柜的隔板不要做到头，留一点空间好让鞋子的灰能漏到最底层，水槽和燃气灶上方装灯。定卫生间地漏的位置时一定要先想好，量好尺寸。地漏最好位于砖的一边，如果在砖的中间位置的话，无论砖怎么样倾斜，地漏都不会是最低点。</p>

                            <p>4、卫生间，空调插座均未设计开关。特别是卫生间电热水器，以一双级开关带一插为宜.如要关去电热，拔插头有危险。 </p>

                    </div>


                           <div>
                               <h4>房屋装修原则</h4>

                               <p>1、均衡对称。均衡是依中轴线、中心点不等形而等量的形体、构件、色彩相配置。均衡和对称形式相比较，有活泼、生动、和谐、优美之韵味。对称是形式美的传统技法，对称又分为绝对对称和相对对称。对称给人感受秩序、庄重、整齐、和谐之美。</p>

                               <p>2、巧用对比。对比是艺术设计的基本定型技巧，把两种不同的事物、形体、色彩等作对照就称为对比。把两个明显对立的元素放在同一空间中，使其既对立又和谐，既矛盾又统一，在强烈反差中获得鲜明的对比，求得互补和满足的效果。例如有些消费者不喜欢用乳胶漆独爱壁纸，但单一的仅仅只使用壁纸又感觉缺少了什么，就可以选择使用硅藻泥背景墙或者壁纸背景墙以形成对比，达到吸引拜访者的视线的目的。 </p>





                               <p>3、家装勿求十全十美。由于受建筑本身缺陷、投资额及施工水平的影响，任何家装都不可能达到完美的程度。而消费者都希望把房子装得完全满意，因此在选择公司时十分挑剔，一些施工单位也为了接单，会故意把方案说得天花乱坠，把业主引入误区，但实际上做出来的家装工程反而并不理想。</p>

                        </div>


